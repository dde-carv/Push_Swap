#include "../../inc/push_swap.h"

static void	rev_rotate(t_stack_node **s)
{
	t_stack_node	*last;

	if(!*s || !(*s)->next)
		return ;
	last = stack_last(*s);
	last->prev->next = NULL;
	last->next = *s;
	last->prev = NULL;
	*s = last;
	last->next->prev = last;
}

void	rra(t_stack_node **a)
{
	rev_rotate(a);
	ft_printf("rra\n");
}

void	rrb(t_stack_node **b)
{
	rev_rotate(b);
	ft_printf("rrb\n");
}

void	rrr(t_stack_node **a, t_stack_node **b)
{
	rev_rotate(a);
	rev_rotate(b);
	ft_printf("rrr\n");
}